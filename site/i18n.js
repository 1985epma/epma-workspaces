function getPageKey() {
  return document.body.dataset.page || 'home';
}

function getSupportedLang(pageKey, lang) {
  return translations[pageKey]?.[lang] ? lang : DEFAULT_LANG;
}

function applyLang(lang) {
  const pageKey = getPageKey();
  const pageTranslations = translations[pageKey] || translations.home;
  const normalized = getSupportedLang(pageKey, lang);
  const map = pageTranslations[normalized] || pageTranslations[DEFAULT_LANG];

  document.documentElement.lang = normalized;
  if (map.title) {
    document.title = map.title;
  }

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    const value = map[key];
    if (value !== undefined) {
      element.textContent = value;
    }
  });

  localStorage.setItem(STORAGE_KEY, normalized);
}

function initLanguageSwitcher() {
  const pageKey = getPageKey();
  const savedLang = localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG;
  applyLang(getSupportedLang(pageKey, savedLang));
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLanguageSwitcher);
} else {
  initLanguageSwitcher();
}
